function randomArrayElement(arr) {
  return arr[Math.floor(Math.random()*arr.length)];
}

function capitaliseFirstLetter(string) {
  return string.charAt(0).toUpperCase() + string.slice(1);
}

function generate() {
  var name = document.getElementById('name'),
      state = '__',
      str = '';
  while(state.indexOf('!') < 0 && str.length < 13) {
    var char = randomArrayElement(MATRIX[state]);
    state = state[1] + char;
    if(state.indexOf('!') < 0) {
      str += char.replace('_',' ');
    }
  }
  
  str = str.replace(/ \w$/,'');
  
  if(str.substr(0, 8).indexOf(' ') < 0 && (str.substr(8).indexOf(' ') < 0 || str.substr(8).indexOf(' ') > 2)) {
      var index = randomArrayElement([3,5,7]);
      str = str.substr(0, index) + ' ' + capitaliseFirstLetter(str.substr(index));
  }
  
  str = str.replace(/ \w$/,'');
  
  name.innerHTML += str+'<br>';
}
document.getElementById('generate').onclick = generate;

function clearAll() {
  document.getElementById('name').innerHTML = '';
  generate();
}
document.getElementById('clear-all').onclick = clearAll;

